using PESYONG.Domain.Entities.Users.Identity;
using PESYONG.Domain.Enums;
using System;
using System.Collections.Generic;
using System.ComponentModel.DataAnnotations;
using System.ComponentModel.DataAnnotations.Schema;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace PESYONG.Domain.Entities.Logistics;

/// <summary>
/// This represents the updates in the Logistics part of the business.
/// </summary>
public class DeliveryUpdate
{
    [Key]
    [DatabaseGenerated(DatabaseGeneratedOption.Identity)]
    public int DeliveryUpdateID { get; set; }

    // Foreign and navigation keys
    [ForeignKey("Delivery")]
    public int DeliveryID { get; set; }
    public virtual Delivery Delivery { get; set; } = new Delivery();

    [ForeignKey("UpdatedByUser")]
    public int? UpdatedByUserID { get; set; }
    public virtual AppUser? UpdatedByUser { get; set; }

    // Delivery update attributes
    [Required]
    public DeliveryStatus Status { get; set; }

    [Required]
    [Column(TypeName = "datetime2")]
    public DateTime UpdateDate { get; set; } = DateTime.Now;

    [Required]
    [StringLength(500)]
    public string UpdateDescription { get; set; } = string.Empty;

    [StringLength(200)]
    public string? Location { get; set; }

    [StringLength(1000)]
    public string? Notes { get; set; }

    [Required]
    [StringLength(100)]
    public AppUser? UpdatedBy { get; set; }
}
